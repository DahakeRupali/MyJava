
class PolyMathOperations {
    // Method with two parameters
    int add(int a, int b) {
        return a + b;
    }

    // Overloaded method with three parameters
    int add(int a, int b, int c) {
        return a + b + c;
    }
    
float add(double d,double e,double f,double g)
    {
    	return (float) (d+e+f+g);
    }
}
